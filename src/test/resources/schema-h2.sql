CREATE TABLE IMAGE (
ID NUMBER(19,0) NOT NULL  AUTO_INCREMENT, 
CREATEDTIME TIMESTAMP (6) NOT NULL, 
EMPLOYEEID VARCHAR2(255 BYTE) NOT NULL, 
IMAGESIZE VARCHAR2(255 BYTE) NOT NULL, 
IMAGETYPE VARCHAR2(255 BYTE) NOT NULL, 
PIC BLOB, 
SCALINGCOMPLETE NUMBER(1,0) DEFAULT 0 NOT NULL, 
SOURCESYSTEM VARCHAR2(255 BYTE) NOT NULL, 
PRIMARY KEY (ID)
);

	
CREATE SEQUENCE IMAGE_ID_SEQ
MINVALUE 1
MAXVALUE 9223372036854775807
START WITH 200
INCREMENT BY 25
CACHE 8
NOCYCLE;